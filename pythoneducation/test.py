from datetime import datetime

# Получаем текущую дату и время
текущее_время = datetime.now()

# Форматируем дату и время в удобочитаемый вид
отформатированное_время = текущее_время.strftime("%d.%m.%Y %H:%M:%S")

# Выводим результат в консоль
print(f"Текущая дата и время: {отформатированное_время}")